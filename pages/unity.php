<p> Unity é um dos principais motores graficos usado atualmente, é bastante conhecido por sua portabilidade,
    podendo portar jogos para Android/iOS/Linux/Windows/Xbox One/Nitendo Switch/PS4 e mais.
    Pode ser usada para desenvolvinto de jogos 3D e 2D.
</p>
<p>
    No unity é possivel criar qualquer tipo de jogo, seu editor é o mais genérico possível, podendo editar cameras e etc. É possível criar scripts em
    C#, quais você pode acoplar a um objeto, como por exemplo em uma camera.

</p>
<p>
    A ideia de componentes facilita o desenvolvimento de um jogo, existem varios componentes prontos, como o <a href="https://docs.unity3d.com/ScriptReference/Rigidbody.html"><b>RigidBody</b></a>,
    usado para aplicar forças no objeto, ou adicionar gravidade.

</p>
<p>
    Seu editor é muito poderoso, é possível editar as variaveis do jogo enquanto testa, adicionar componentes, ver posições de objetos e mais,
</p>
