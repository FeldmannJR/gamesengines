<p> A Unreal Engine é desenvolvida pela Epic Games, vista pela primeira vez em 1998, primeiramente foi desenvolvida para FPSs,
    mas foi usada para uma variedade de tipos de jogos. Seu código é escrito em C++.
</p>
<p>
    A versão atual da Unreal é a 4, com a 1 sendo lançada em 1998, continha renderização, detecção de colisão, IA, e também um sistema cliente-servidor.
</p>
<p>
    A versão atual sem duvida é uma das mais poderosas em questão grafica da atualidade.
</p>

