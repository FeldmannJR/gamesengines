<h2>Blueprints</h2>
<p>Para desenvolver na unreal não é necessário saber programação, pois ela conta com um sistema de blueprints, uma espécie de fluxograma onde é possível captar eventos, criar funções e mais.</p>
<p>Claro que para jogos mais complexos é necessário conhecimento de C++, pois é nesta linguagem que são criados os scripts para os jogos.</p>

