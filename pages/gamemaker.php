<p>
    O Game Maker foi criado por Mark Overmars, que lançou a primeira versão do programa em 15 de novembro de 1999, destinado a ser um programa de animação 2D,
    mas como alguns de seus utilizadores usavam o programa com o propósito de criar jogos, e não animações, Overmars decidiu mudar seu nome
</p>
<p>
    Todos os recursos dos jogos são organizados em pastas dentro do programa, que inclui pequenos programas para criar seus recursos, como editores de imagens, sons, scripts e fases. O GameMaker: Studio permite ainda salvar os recursos criados
    para que possam ser usados em outros jogos ou fora do programa e importar ações adicionais para estender as funções do programa.
</p>


